#include "ft_irc.hpp"

bool	is_special(char c) {
	if (c == '[' || c == ']' || c == '\\'
		|| c == '`' || c == '_' || c == '^'
		|| c == '{' || c == '|' || c == '}') {
		return (true);
	}
	return (false);
}